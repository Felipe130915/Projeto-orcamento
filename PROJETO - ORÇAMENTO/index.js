
function Soma1(){  /* FUNÇÃO QUE FAZ A MULTIPLICAÇÃO DOS INPUS DA QUANTIDADE E DO VALOR INITARIO DOS PRODUTOS*/
    var soma = 0;
    var ipts = document.querySelectorAll('input[oninput="Soma1()"]');
    for(var x=0; x<ipts.length; x++){
       var valorItem = parseFloat(ipts[x].value);
       if(soma ===0)
          soma = valorItem;
       else{
         !isNaN(valorItem) ? soma *= parseFloat(valorItem) : null;
         console.log(soma);
       }
    }
    document.querySelector('#Total1').value = soma.toFixed(2);
 }

