function clicar() {
   var Total1 = document.querySelector(".Total1").value;  /* total1 Representa a class do input*/
   var Total2 = document.querySelector(".Total2").value; /* total2 Representa a class do input*/

// caculo do desconto
   var Recebedesconto = document.querySelector(".Desconto").value; /* Recebe o valor do desconto*/
   var Dividedesconto = 100;
   var produtosdesconto = parseFloat(Total1.replace(',', '.')) + parseFloat(Total2.replace(',', '.'));  // replace(',', '.') usado para os inputs aceitarem numeros co virgula
   var Transformaemporcentagem = Recebedesconto / Dividedesconto;
   var Desconto = produtosdesconto * Transformaemporcentagem;
 // calculo do desconto

   var Total_produtos = parseFloat(Total1.replace(',', '.')) + parseFloat(Total2.replace(',', '.')) - Desconto;
   var comissao = 0.10; /*var que calcula a comissão de 10%*/
   var Servico = Total_produtos * comissao;

   
   var resultado = Total_produtos + Servico;
   document.querySelector(".resultado").value = resultado. toFixed(2); /* resultado Representa a class do input "importante para o valor do total ficar dentro de um input e necessario usar   .value = resultado. toFixed(2); */

}





